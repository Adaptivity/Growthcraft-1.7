package growthcraft.api.cellar.booze;

public class PotionEntry
{
	private int id;
	private int time;
	private int level;

	public PotionEntry(int i, int t, int l)
	{
		this.id = i;
		this.time = t;
		this.level = l;
	}

	public int getID()
	{
		return id;
	}

	public int getTime()
	{
		return time;
	}

	public int getLevel()
	{
		return time;
	}
}
